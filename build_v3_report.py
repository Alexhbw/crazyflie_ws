H("3.3 绝对稳定域映射", 2)

P("对于任意显式 ODE 单步数值解法，其对线性测试方程 ẏ = λy 的离散迭代格式均可归结为：")
F("y_{n+1} = R(hλ) · y_n")

P("其中 R(z) 为该算法的稳定性函数。不同求解器的 R(z) 决定了它们的稳定域形状和大小：")
