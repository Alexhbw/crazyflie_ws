F("RK4:    R(z) = 1 + z + z²/2 + z³/6 + z⁴/24")

P("绝对稳定域 S 定义为复平面上使放缩因子模长严格小于 1 的区域：")
F("S = { z ∈ C : |R(z)| < 1 }")

P("系统的数值稳定充要条件是所有特征值经 h 缩放后均落入 S 内：")
F("∀i:  h·λ_i ∈ S")

P("对于显式方法（Euler、RK4），S 是复平面上围绕 (-1, 0) 的有限区域；"
  "对于 A-稳定的隐式欧拉法，S 覆盖整个左半复平面（C^- ⊂ S），因此不受上述约束。")

H("3.4 安全临界步长确界定理", 2)

P("基于稳定域映射准则，给出各求解器的理论临界安全步长 h_crit：")
