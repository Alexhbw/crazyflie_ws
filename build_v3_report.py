H("11.3 核心算法", 2)

P("(1) 指数平滑——防止单步误触发")
P("每步的安全分数可能因瞬态扰动（如阵风、编队收缩）而出现短时下降。"
  "使用指数移动平均 (EMA) 对分数进行低通滤波：")
F("smoothed_score = α · overall_score + (1-α) · smoothed_score")
P("其中 α = 0.05，等效于约 20 步的记忆窗口。只有持续的分数下降才会触发响应，"
  "单步尖峰被有效滤除。")

P("(2) 评估调度——降低检查开销")
P("SafetyController 每 5 步执行一次评估（而非每步）。在 dt=0.01 的典型运行中，"
  "这意味着每秒评估 20 次，远频繁于任何实际安全变化的速率，同时避免每步都调用"
  "getScores() 和 evaluate() 的微小开销。")

P("(3) 步长减半——最温和的干预")
F("new_dt = max(current_dt × 0.5, dt_min)")
P("dt_min = 0.0001 s，防止无限减半至零。减半后通过 create_wall_timer 重建定时器。")

P("(4) 求解器切换——中等强度的干预")
P("调用 saferSolver(current) 返回下一个更安全的求解器枚举值，"
  "然后调用 buildSolverDispatch() 重建 SolverFn 函数指针。"
  "由于统一调度的设计，切换求解器不需要任何代码分支改动。")

P("(5) 紧急关机——最后手段")
P("当 smoothed_score < thresh_stop = 0.05 时，认为系统已不可救——"
  "可能是数值发散至 NaN、所有无人机均已碰撞障碍物、或姿态失控至无法恢复的程度。"
  "此时执行 safety_monitor->writeCSV() 保存数据后调用 rclcpp::shutdown()。")

