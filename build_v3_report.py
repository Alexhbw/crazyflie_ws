H("2.2 各算法详解", 2)

P("每种求解器代表了不同的精度-稳定性-计算量权衡策略。选择合适的求解器"
  "对无人机的安全仿真至关重要——这是本研究后续通过实验定量验证的核心问题。")

H("2.2.1 前向欧拉法 (Forward Euler)", 3)
P("前向欧拉法是最简单的显式单步法，利用当前点的导数值近似整个步长区间内的变化率：")
F("x_{n+1} = x_n + h · f(x_n, u_n)")
P("每步仅需 1 次动力学函数求值，计算量最小。但一阶精度意味着局部截断误差为 O(h²)，"
  "全局误差为 O(h)。条件稳定特性使其在大步长或刚性系统中容易发散。"
  "在本系统中，前向欧拉法在大约 h = 0.03 s 时开始出现速度超标和姿态发散。")

H("2.2.2 改进欧拉法 / Heun 法 (RK2)", 3)
P("改进欧拉法采用 [预测-校正] 模式，有效降低局部截断误差至 O(h³)：")
F("k1 = f(x_n, u_n)")
F("k2 = f(x_n + h·k1, u_n)")
F("x_{n+1} = x_n + (h/2)·(k1 + k2)")
P("通过计算起点和预测点两个位置的平均斜率，二阶精度使轨迹平滑度显著优于前向欧拉法，"
  "而计算量仅增加一倍（2 次求值 vs 1 次）。")

H("2.2.3 经典 RK4 法", 3)
P("四阶 Runge-Kutta 法是工业仿真的事实标准，通过四个精心设计的采样点实现 O(h^5) 局部截断误差：")
F("k1 = f(x_n, u_n)")
F("k2 = f(x_n + h/2·k1, u_n)")
F("k3 = f(x_n + h/2·k2, u_n)")
F("k4 = f(x_n + h·k3, u_n)")
F("x_{n+1} = x_n + (h/6)·(k1 + 2k2 + 2k3 + k4)")
P("RK4 的权重设计 (1/6, 1/3, 1/3, 1/6) 恰好抵消了泰勒展开中 h⁴ 以下的全部误差项。"
  "固定步长和确定性的单步计算量使其非常适合实时控制系统的离线仿真验证。")

