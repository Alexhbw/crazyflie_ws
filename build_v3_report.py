H("八. 区间可达集近似分析 (Interval Reachability)", 1)

H("8.1 问题定义与理论背景", 2)

P("在形式化验证中，可达集分析回答以下问题：给定系统的当前状态 x 和有界的不确定性 ε，"
  "在一步（或多步）之后，系统可能到达的所有状态的集合是什么？如果这个集合与障碍物区域"
  "的交集为空，则系统在当前不确定性下 [绝对不可能] 发生碰撞——这是形式化安全保证"
  "的最强形式之一。")

P("与第四章中讨论的 CORA 全局可达性分析不同，IntervalReachability 的设计目标是"
  "轻量级、在线运行——直接嵌入在 C++ 求解器层面，不需要调用外部 MATLAB 工具箱，"
  "在每一步积分后即可给出 [最坏情况] 的安全判定。这种设计使其适用于在线安全控制"
  "场景：控制器可以用可达集分析的结果来决定是否需要采取保护性动作。")

P("数学模型：给定当前状态 x ∈ R^12、控制输入 u ∈ R^4 和步长 h，定义扰动半径 ε > 0，"
  "考虑初始状态的不确定集：")
F("X_0 = { x + δ : |δ_j| ≤ ε, j = 0..11 }")

P("经过一步数值积分后，前向可达集 F(X_0) 是所有这些不确定初始条件下求解器输出的集合：")
F("F(X_0) = { solver(x + δ, u, h) : |δ_j| ≤ ε, j = 0..11 }")

P("由于 F 是非线性映射（solver 是高度非线性的），精确计算 F(X_0) 在计算上不可行。"
  "本方法计算 F(X_0) 的一个保守外逼近——一个三维位置区间包围盒 [p_low, p_high]，"
  "满足 F(X_0) ⊆ [p_low, p_high]。然后检测此包围盒是否与任何障碍物相交。")

H("8.2 方法一：采样法 (Sampling Method)", 2)

P("采样法是最直接且保守性最强的方法。对 12 维状态空间的每一维 j，分别施加正负两个方向"
  "的最大扰动 ±ε·e_j，运行求解器得到该扰动下的输出状态，然后对所有输出取逐维的最小值和最大值：")

F("x_center = solver(x, u, h)")
F("For j = 0..11:")
F("  x_plus_j  = solver(x + ε·e_j, u, h)")
F("  x_minus_j = solver(x - ε·e_j, u, h)")
F("For k = 0..11:")
F("  lower_k = min(x_center_k, min_j(x_plus_jk, x_minus_jk))")
F("  upper_k = max(x_center_k, max_j(x_plus_jk, x_minus_jk))")

P("采样法每次调用需要进行 25 次求解器计算：1 次中心点 + 12 维 × 2 方向。"
  "它的保守性来源于对非线性函数的 [穷举式采样]——虽然不是所有可能的 δ 都被测试，"
  "但沿着每个坐标轴的极值方向通常对应最大的输出偏差。在大多数实际系统中，"
  "采样法给出的包络确实包含了 F(X_0)。")

P("优点：实现简单、保守性强、不依赖任何线性化假设。")
P("缺点：25 次求解器调用带来的计算开销——对于隐式欧拉法 (每步 65 次求值)，"
  "加上采样法可达集分析将变为每步每机 90 次动力学求值（×4 机 = 360 次），"
  "在高频仿真中不可接受。")

H("8.3 方法二：Jacobian 法 (Jacobian Method)", 2)

P("Jacobian 法通过有限差分计算一步状态转移的 Jacobian 矩阵，然后利用矩阵的绝对值"
  "进行线性化区间传播。相比采样法，它将求解器调用次数从 25 次降至 13 次。")

P("第一步：通过中心差分近似 12×12 Jacobian 矩阵的每一列：")
F("For j = 0..11:  J[:,j] = [solver(x + ε·e_j) - solver(x - ε·e_j)] / (2ε)")

P("第二步：取 Jacobian 各元素的绝对值，构建 |J| 矩阵。然后进行线性区间传播：")
F("Δ = |J| · ε · 1_12")
F("lower = x_center - Δ")
F("upper = x_center + Δ")

P("线性传播的直觉：|J_{i,j}| · ε 是第 j 维的 ε 扰动通过系统的局部线性化对第 i 维输出"
  "产生的最大影响。将所有维度的贡献求和（|J| · ε · 1），得到各维输出的最坏情况偏差。")

P("Jacobian 法的保守性略低于采样法（线性化忽略了高阶效应），但计算量几乎减半。"
  "在实际应用中，两种方法可以按需切换：正常的巡航仿真用 Jacobian 法，"
  "在障碍物附近或安全分数下降时切换到采样法进行更严格的分析。")

